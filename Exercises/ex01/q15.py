num1 = int(input("Informe o primeiro número inteiro: "))
num2 = int(input("Informe o segundo número inteiro: "))
num_mult = num1 * num2
num_div = num1 / num2
num_soma = num1 + num2
num_sub = num1 - num2
print(f"Produto de {num1} e {num2} corresponde a {num_mult}")
print(f"Divisão de {num1} por {num2} corresponde a {num_div}")
print(f"Soma de {num1} e {num2} corresponde a {num_soma}")
print(f"Subtração de {num1} por {num2} corresponde a {num_sub}")
